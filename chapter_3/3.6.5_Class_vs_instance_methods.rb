# 3.6.5 Class methods vs Instance methods

#Remember:
#  - Classes are objects.
#  - Instances of classes are objects, too.
#  - A class object (like Ticket ) has its own methods, its own state, and its own iden-
#  tity. It doesn’t share these things with instances of itself. Sending a message to Ticket
#  isn’t thi same thing as sending a message to fg or cc or any other instance of Ticket
